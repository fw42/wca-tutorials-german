<p>
Du möchtest an deinem ersten Speedcubing-Wettbewerb ("Competition") teilnehmen, aber weißt
nicht wie es dort so abläuft? Diese Seite soll dir eine kleine Hilfe sein, damit deine erste
Competition ein schönes Erlebnis wird. Jede Competition ist anders, aber die Grundabläufe
sind bei allen Competitions gleich, da sie stets nach dem WCA Regelwerk ablaufen. Die WCA
ist eine internationale Organisation für das Speedcubing, etwa wie die FIFA für den Fußball.
Auf jedem Turnier ist mindestens ein offizieller WCA Delegierter anwesend, der die Einhaltung
der Regeln überwacht und stets gerufen werden sollte, wenn jemand nicht genau weiß, was zu tun ist.
</p>

<p>Achtung! Dies nur eine vereinfachte Zusammenfassung für Neulinge. Vor der Teilnahme an einem
Turnier sollte jeder Teilnehmer die vollständigen WCA Regulations gelesen haben. Eine deutsche
Übersetzung gibt es hier: <a href="https://www.worldcubeassociation.org/regulations/translations/german/">https://www.worldcubeassociation.org/regulations/translations/german/</a>.</p>

<h2>Informiere dich vorher!</h2>

<p>Bevor du auf ein Turnier gehst, lies dir die Webseite des Turniers aufmerksam durch. Diese
enthält wichtige Informationen, z.B. den Zeitplan, Zeitlimits, die du erreichen musst und
vieles mehr. In der Regel verschicken Turnierorganisatoren kurz vor einem Turnier Rundmails.
Auch diese solltest du aufmerksam lesen.</p>

<h2>Puzzle</h2>

<p>
Auf WCA Turnieren sind die meisten Puzzle erlaubt (siehe Abschnitt 3 der WCA Regeln). Wichtig ist,
dass deine Puzzle sich in einem guten Zustand befinden. Sind die Sticker auf deinem Cube also sehr
abgenutzt, so solltest du überlegen, diese gegen neue zu ersetzen. Besonders beim Blindlösen kann
es sonst passieren, dass du mit deinem Puzzle nicht teilnehmen darfst.
</p>

<h2>Plane deinen Tag</h2>

<p>
Der Zeitplan eines Turniers ist vorab auf dessen Webseite verfügbar. Schaue dir an, wann die
Events, für die du angemeldet bist, stattfinden und sei pünktlich zum Anfang jedes dieser Events
bereit teilzunehmen.
</p>

<h2>Warte bis dein Name aufgerufen wird</h2>

<p>
Teilnehmer eines Events werden meistens in Gruppen eingeteilt, die du den ausgehängten Listen
entnehmen kannst. Sobald dein Name oder deine Gruppe aufgerufen wird, gehe zum Scramblingtisch
und lege dein Puzzle auf den Zettel (Scoresheet), auf dem dein Name steht. Danach setzt
du dich in den Wartebereich. Wenig später wird dich ein Judge aufrufen. Gehe zusammen mit ihm
zu einem der Timertische und setze dich. Der Judge legt das Puzzle vor dir auf die Matte
(Stackmat). Zu Beginn des Versuchs ist es mit einer Abdeckung verdeckt. Dein Judge wird dich
nun fragen, ob du bereit bist ("Ready?"). Falls du zu diesem Zeitpunkt noch unsicher bist, was
den weiteren Ablauf angeht (z.B. wenn du nicht weißt, wie der Timer funktioniert), zögere nicht
den Schiedsrichter einfach zu fragen. Sobald du aber eindeutig bejahst ("Ja!"), dass du bereit
bist, wird er die Abdeckung hochheben, womit die Inspection Phase beginnt.</p>

<h2>Inspection und Beginn des Versuchs</h2>

<p>
Du hast nun bis zu 15 Sekunden Zeit, dir das Puzzle anzuschauen und den Anfang deiner Lösung
zu planen. In dieser Zeit passiert folgendes: Du darfst das Puzzle in der Hand halten, aber
keine Drehungen machen. Dein Judge wird dir nach 8 Sekunden ("8 seconds") und
12 Sekunden ("12 seconds") die Zeit ansagen. Du kannst allerdings jederzeit in der Inspection
Phase den Versuch starten. Dazu legst du das Puzzle wieder auf die Matte und startest den Timer,
indem du beide Hände <i>flach</i> auf die Sensoren legst. Warte kurz bis die grüne Lampe
leuchtet. Wenn du nun die Hände herunternimmst, läuft die Zeit und du kannst anfangen das Puzzle
zu lösen. Spätestens nach den 15 Sekunden musst du den Timer gestartet haben.
</p>

<h2>Ende des Versuchs</h2>

<p>
Wenn du das Puzzle gelöst hast, lege das Puzzle auf den Tisch und stoppe den Timer. Dazu musst du
wieder die Hände <i>flach</i> auf die Sensorflächen legen. Achte darauf, dass du das Puzzle dabei
nicht mehr berührst. Dein Judge wird nun überprüfen, ob das Puzzle gelöst ist und deine Zeit
aufschreiben. Er wird dir auch sagen, falls es Regelverstösse gab. Warte dies ab und berühre
währenddessen nicht das Puzzle. Anschliessend musst du auf dem Scoresheet unterschreiben um das
Resultat zu bestätigen. Bevor du dies tust, vergewissere dich dass die Zeit richtig aufgeschrieben
wurde. Wenn du unsicher bist, zögere auch hier nicht nachzufragen oder sogar den WCA Delegierten
herbeizurufen.</p>

<p>Wenn du noch weitere Versuche machen darfst, wird der Judge das Puzzle mitnehmen und zurück zum
Scramblingtisch bringen. Du musst dich dann zurück in den Wartebereich setzen. Ansonsten ist das
Event für dich vorbei und du kannst dein Puzzle wieder mitnehmen.
</p>

